
class OptionalDemo1 {
	static void main(String[] args) {
		
		//Example of an Integer Using def
		def a = 100;
		println(a);
		
		//Example of an float using def
		
		def b = 100.10;
		println(b);
		
		//Example of using double def
		def c = 100.101;
		println(c);
		
		//Example of an string def
		def d = "Hello world";
		println(d);
	}
}
